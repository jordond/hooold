package ca.hoogit.hooold;

import android.content.Context;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

/**
 * @author jordon
 *
 * Date    11/08/15
 * Description
 *
 */
public class FragmentAdapter extends FragmentPagerAdapter {

    private String[] mTitles;

    public FragmentAdapter(Context context, FragmentManager fragmentManager) {
        super(fragmentManager);
        mTitles = context.getResources().getStringArray(R.array.tabs);
    }

    @Override
    public Fragment getItem(int position) {
        if (position < mTitles.length) {
            return MessageFragment.newInstance(mTitles[position], "Test " + position);
        }
        return null;
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return mTitles[position];
    }

    @Override
    public int getCount() {
        return mTitles.length;
    }

}
